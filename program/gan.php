                            <div class="detail-box">
                                <div class="heading_container spon_padding">
                                    <h2>
                                        <span>Abstract</span>
                                    </h2>
                                </div>
                                <p style="text-align: justify;">
                                    Future trends for next generation strategic systems require multifunctionality and modularity like combining radar, communications, and electronic warfare in one system. This higher level of functional integration improves system performance through heightened awareness, improved responsiveness, and mission execution. The use of gallium nitride (GaN) MMIC as key component enables higher performance of systems meeting the requirements in small size with high power and hence GaN MMIC are emerging as an alternative or replacement for laterally diffused MOSFET (LDMOS) components. In most of next generation systems GaN MMIC technology is being considered because of high power density, high efficiency, wide bandwidth, and exceptionally long life. GaN on SiC has superior properties like higher breakdown voltage; higher saturated electron drift velocity and higher thermal conductivity. Hence GaN HEMTs also offer greater power density and wider bandwidths compared to Si; GaAs; and GaN on Si transistors. As shorter gate length GaAs and GaN transistors become available, coupled with improved circuit design techniques, new devices are becoming available that can perform comfortably to millimeter wave frequencies, opening new applications that were hard to contemplate a decade ago. This session will briefly describe the state of the semiconductor technology that is enabling these developments to achieve optimum performance of devices, circuits and subsystems based on this technology.
                                </p>
                                <div class="heading_container spon_padding">
                                    <h2>
                                        <span>Session Chair:</span>
                                    </h2>
                                </div>
                                <p style="text-align: left;">
                                    Dr S Christopher, Former Chairman of the Defence Research and Development Organisation (DRDO). Presently professor in Electrical Science Department at India Institute Technology, Madras.
                                </p>
                                <div class="heading_container spon_padding">
                                    <h2>
                                        <span>GaAs and GaN MMIC s for strategic Applications</span>
                                    </h2>
                                    <h1>
                                        Dr Suma Varughese, Outstanding Scientist, Director General MED, CoS&CS (MCC).
                                    </h1>
                                </div>
                                <p style="text-align: left;">
                                    Gallium Arsenide (GaAs) and Gallium Nitride (GaN)-based  technologies are revolutionizing the modern defence RF and space application. The capabilities to deliver high power, high frequency, high linearity, high efficiency and high temperature performance renders it the most sought after device for applications in advanced radars, data links, satcoms, etc. GaAs and GaN Monolithic Microwave Integrated Circuits (MMICs) are key enablers for the ever shrinking defence and space systems. GaN-based HEMT technology offers a significant advantage over the existing gallium arsenide (GaAs) Monolithic Microwave Integrated Circuits (MMICs) due to the capability of GaN devices to operate at higher voltages owing to very high breakdown fields associated with them. Additionally, the GaN devices offer much higher impedance resulting in the requirement of less complex matching networks in RF power amplifier integrated circuits. The low current operation aided with a higher efficiency results in power saving and reduced costs for cooling the system. Therefore, the GaN-based power amplifiers constitute the heart of present day transceiver (T/R) modules in AESA (Active Electronically Scanned Array) radars and communication systems.
                                </p>

                                <div class="heading_container spon_padding">
                                    <h2>
                                        <span>Status of Indigenous GaN HEMT based MMIC Technology  </span>
                                    </h2>
                                    <h1>
                                        D. S. Rawal, Solid State Physics Laboratory, Delhi, India
                                    </h1>
                                </div>
                                <p style="text-align: left;">
                                    Advancement in III-N device technology have mainly taken place due to material innovations that paved the way for the development of new generation of microwave devices based on confinement of carrier in quantum well with superior transport properties. Consequently AlGaN/GaN HEMT (High Electron Mobility Transistor) based MMICs (Monolithic Microwave Integrated Circuit) with improved performance are being developed worldwide for high frequency, high power, and broadband civil/military systems. Indigenous HEMT MMIC technology is developed on 100µm thick, 75mm diameter SiC semi-insulating substrate, to deliver RF power output ~ 5W/mm @28V for up to X-band applications. The main technology breakthrough has taken place in HEMT device design, epi-layer structure, improved S/D contacts, reduced gate length (< 0.2µm) with gate engineering, innovative multi-finger field plate design and low parasitic interconnection techniques using air-bridge metal cross overs/through substrate via-hole source grounds. Following device development, extensive device characterization is done, Process Design Kit (PDK) specific to indigenous foundry capability is developed and C/X band Power amplifier, Low noise amplifier and Switch MMICs are designed and fabricated.  The talk will mainly cover all the important aspects of GaN HEMT device technology right from HEMT material structure optimization, device design to unit process development on front side/backside of wafer to fabricate active/passive component, characterization and their integration including PDK development suitable for X-band MMIC applications.  
                                </p>

                                <div class="heading_container spon_padding">
                                    <h2>
                                        <span>Development of Indigenous Process Design Kit for GaN MMIC</span>
                                    </h2>
                                    <h1>
                                        Mr Samuder Gupta, Sc-G,SSPL
                                    </h1>
                                </div>
                                <p style="text-align: left;">
                                    SSPL has successfully developed process design kit for the indigenous GaN MMIC technology  process. This process design kit is required for MMIC design involving power amplifiers, low noise amplifiers SPDT switches and multifunctional MMICs in various frequency ranges. The kit contains passive and active components required for the design.
                                </p>

                                <div class="heading_container spon_padding">
                                    <h2>
                                        <span>Development of Indigenous GaN MMIC</span>
                                    </h2>
                                    <h1>
                                        Dr Meena Mishra, Sc-G,SSPL,DRDO
                                    </h1>
                                </div>
                                <p style="text-align: left;">
                                    SSPL/GAETEC have successfully demonstrated GaN technology at C band, X band and is presently working towards the development of Ku band applications. 130W discrete GaN power bar , GaN based C band X band 10W,20W and 30W power amplifiers, C-Ku band and Ku band Power amplifiers and multifunctional MMIC, low noise amplifiers SPDT MMICs have been successfully demonstrated  The RF characterisation has been carried out in all the X band circuits in pulse mode and  for C-Ku band and Ku band circuits in pulse mode as well as CW mode.  Out of these circuits C-Ku band power amplifier on 0.25 µm node , C-Ku band TR chip and Ku band TR chip based on 0.15 µm node are the circuits designed with innovative techniques and are not off the shelf available components.
                                </p>
