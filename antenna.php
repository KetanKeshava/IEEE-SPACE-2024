                    <b>9.30AM-10.30AM: Performance of Large Compensated Compact Range Facilities for Antenna and Payload Testing of State-of-the-Art and Future Spacecrafts: Josef Migl, Airbus Defence and Space GmbH, Munich, Germany</b><br>
                    Large compensated compact ranges are designed for antenna and payload testing of spacecraft antennas and payload units. The Compensated Compact Range concept (CCR) of Airbus is featuring two major advantages for such measurements. First of all, the compensating reflector design of the CCR delivers lowest cross-polarization (< -40 dB) for frequencies â‰¥ 3 GHz and its short effective focal length provides advantageous scanning capability of the test zone. The very low cross-polar contribution of the measurement range is a mandatory aspect for testing antennas utilized for telecommunications satellites due to their stringent cross-polar specifications. The scanning capability is of interest for deployed antennas of spacecrafts and allows measurements without relocation of the complete spacecraft for moving the Antenna under Test (AUT) into the centered test zone. This feature is already highly interesting for antenna testing, but much more for end-to-end testing campaigns where it provides a fully radiated test scenario for up- and downlink without touching the spacecraft at all. In order to achieve large test zones Airbus developed already a few years ago a new, larger type of compensated compact range, the CCR 120/100 with a centered test zone size of 8 m and scanned test zone diameters of at least 5 m for off-axis locations. The latest development and installation here in India even go above this scenario and reaches up to 10 m test zone aera for the centered test zone and 7 m for the scanned areas. This design allows the testing of all state-of-the-art spacecrafts within the centered test zone with the maximum available performance over the entire test zone area and provides sufficient margin for future spacecraft designs. Within this presentation performance data of recently installed, extremely large compensated compact-range test facility will be presented. The design criteria were mainly determined by the stringent requirements for antenna and payload testing of current and future communication satellites including broadband testing at low frequencies for navigation applications.<br><br>

