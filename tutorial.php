                <h2 style="text-align: center;">
                    TUTORIAL on <span>Treatment of Some Fundamental Topics</span> in <span>Antenna Theory </span>
                </h2><br>
                <h2>Date: Dec 12, 2024, 8.30-17.30 Hall: Bidar</h2>

                <div class="table-responsive">
                    <table class="table table-bordered border-primary text-justify">
                        <thead>
                            <tr>
                                <th scope="col">Time (Hrs)</th>
                                <th scope="col">Topics</th>
                                <th scope="col">Description</th>
                            </tr>
                        </thead>
                        <tbody>
                            <tr>
                                <th scope="row">08.30 - 9.30</th>
                                <td>Brief introduction to notation and some preliminary concepts including charge as a
                                    fundamental source of EM fields:<b> Prof. Prabhakar Pathak, OSU, USA</b></td>
                                <td>
                                    <ol type="a">
                                        <li>Current flow viewed as net motion of charges. Development of complete
                                            expressions for the exact EM fields of general current distributions.</li>
                                        <li>Various approximations for the corresponding fields of antennas in their
                                            reactive, Fresnel and Fraunhofer regions, respectively.</li>
                                        <li>Complex EM power conservation theorem and its use in developing a general
                                            expression for antenna impedance. Use of the antenna impedance in describing
                                            an equivalent circuit for a transmitting antenna.</li>
                                    </ol>
                                </td>
                            </tr>
                            <tr>
                                <th scope="row">9.30 -10.30</th>
                                <td>Development of EM Reciprocity and Reaction Theorems for Antennas in the frequency
                                    domain:<b> Prof. Prabhakar Pathak, OSU, USA</b></td>
                                <td>
                                    <ol type="a">
                                        <li>Field/Lorentz form of reciprocity theorem. </li>
                                        <li>Development of circuit form of the reciprocity theorem for a pair of coupled
                                            antennas. Concept of mutual impedance/admittance. A two port network for
                                            representing a pair formed by a transmitting and a receiving antenna system.
                                        </li>
                                        <li>Generalized reciprocity/reaction theorem in mixed circuit-field form leading
                                            to specific field based expressions for calculating the mutual
                                            impedance/admittance between a pair of antennas. Examples involving coupled
                                            dipoles, as well as coupled slots in a ground plane are discussed.</li>
                                    </ol>
                                </td>
                            </tr>
                            <tr>
                                <th scope="row">10.30 -11.00</th>
                                <td colspan="2"><b>Tea Break</b></td>
                            </tr>
                            <tr>
                                <th scope="row">11.00-12.00</th>
                                <td>Additional applications of generalized reciprocity/reaction theorems: <b> Prof.
                                        Prabhakar Pathak, OSU, USA</b></td>
                                <td>
                                    <ol type="a">
                                        <li>Reaction (generalized reciprocity) theorem for calculating the voltage
                                            induced in a receiving antenna from a distant/far zone transmitting antenna.
                                        </li>
                                        <li>Thevenin/Norton circuit for a receiving antenna.</li>
                                        <li>Reaction theorem for calculating the far zone radiation pattern of a patch
                                            antenna without the use of a complicated Sommerfeld integral form of the
                                            microstrip Green’s function.</li>
                                    </ol>
                                </td>
                            </tr>
                            <tr>
                                <th scope="row">12.00-13.00</th>
                                <td>Slotted rectangular waveguide array analysis: <b> Prof. Prabhakar Pathak, OSU,
                                        USA</b></td>
                                <td>
                                    <ol type="a">
                                        <li>Resonant (narrow band ) broad-wall slotted waveguide array theory for
                                            generating a broadside beam. </li>
                                        <li>Non resonant broad-wall slotted waveguide array as a leaky wave antenna for
                                            frequency scanned beam applications. Forward and backward beams.</li>
                                    </ol>
                                </td>
                            </tr>
                            <tr>
                                <th scope="row">13.00 -14.00</th>
                                <td colspan="2"><b>Lunch Break</b></td>
                            </tr>
                            <tr>
                                <th scope="row">14.00-15.00</th>
                                <td>Electrification - What the future holds for an EMC Engineer? <b>Dr. Vignesh Rajamani, President (2024-2023), IEEE Electromagnetic Compatibility Society</b></td>
                                <td>
                                    <p>
                                    Energy is central to nearly every major challenge and opportunity the world faces today, and countries are racing to help mitigate climate change and invest in energy models that are not only clean but sustainable. The transformation of transportation by using batteries as form of energy source is transformative and disruptive, if not existential. The engineering challenges that come along with electrification is huge and the opportunities are even bigger. Making electric components that can operate efficiently while handling the higher voltages and currents and protecting them against Electromagnetic Interference both from the external and internal electromagnetic environment guarantees a bright future for Electromagnetic Compatibility Engineers around the world. This talk will discuss some challenges and most importantly the opportunities that exist in the field of EMI/C when it comes to the future of e-mobility and electrified transportation.
                                    </p>
                                </td>
                            </tr>
                            <tr>
                                <th scope="row">15.00-16.00</th>
                                <td>Constructive Parameters of Metamaterial: Lorentz Versus Floquet Models : <b>Arun K.
                                        Bhattacharyya, Fellow IEEE, Center of Excellence, Lockheed Martin Space,
                                        Littleton, Colorado, USA.</b></td>
                                <td>
                                    <p>
                                        An array of conducting obstacles in a host dielectric medium modifies the
                                        constructive
                                        parameters of the composite medium. Typically, the parameters are estimated
                                        using Lorentz model. The Lorentz model predicts that an array of metallic
                                        obstacles modifies both permeability and permittivity tensors. Recently, a
                                        rigorous Floquet eigenmodal formulation was developed to analyze metamaterials.
                                        Contrary to the Lorentz model, the Floquet model predicts that the perfectly
                                        conducting obstacles modify the permittivity tensor, but do not modify the
                                        permeability tensor of the metamaterial. Interestingly, numerical results
                                        obtained from two independent full wave analyses of metamaterial-slabs support
                                        the Floquet model. In this presentation, we describe both models and examine the
                                        source of discrepancy between the models. We also discuss whether a metamaterial
                                        with negative permeability is achievable using conducting obstacles.

                                    </p>
                                </td>
                            </tr>
                            <tr>
                                <th scope="row">16.00 -16.30</th>
                                <td colspan="2"><b>Tea Break</b></td>
                            </tr>
                            <tr>
                                <th scope="row">16.30-17.30</th>
                                <td>Chipless RFID, IoT, and 5G and Beyond Technologies for the 21st Century: <b>Dr.
                                        Nemai Chandra Karmakar, Director, Monash Microwave, Antenna, RFID and Sensor
                                        Laboratory (MMARS), Director, ECSE Industry Engagement,
                                        Monash University, Australia </b>
                                    </b></td>
                                <td>
                                    <p>
                                        The post-COVID-19 21st -century era has witnessed tremendous impetus in low
                                        latency high data rate wireless communications. When people were in lockdowns,
                                        the vital means for wireless communications with video content are smartphones
                                        using 4/5G and beyond communications. Now, this tradition continues and becomes
                                        a new normal in society. The vital building blocks to support such high data
                                        rate mobile communications are ultrawideband and/or multiband
                                        RF/microwave/mm-wave transceivers, smart antennas, signal processing algorithms
                                        and digital design. The Internet of Things (IoT) plays a vital role in fluidity
                                        and transparency in data communications over the Internet. The backbone of IoT
                                        is radiofrequency identification (RFID) technology. Due to the
                                        application-specific integrated circuits (ASICs) in the RFID tag, the cost of
                                        the identification and tracking system has become formidably expensive for mass
                                        deployment. The chipless RFID, which is a technology in-between the RFID and
                                        optical barcodes, has wireless sensing capability and can shade new light in the
                                        IoT infrastructure. Microwave and mm-wave ultrawideband technologies are
                                        essential elements for chipless RFID readers, signal processing and encoding and
                                        decoding techniques.

                                        This special tutorial session will provide comprehensive details of the UWB
                                        microwave and mm-wave transceiver design, the multiple input multiple output
                                        (MIMO) smart antenna system, various chipless RFID tags and signal processing
                                        algorithms. Along with this, the 5G and beyond technologies, augmented with
                                        monolithic microwave integrated circuits (MMIC) GaAs and GAN-based phase
                                        shifters and switching elements, have accelerate the high data rate wireless
                                        communications. This tutorial presents these cutting edge technologies
                                        automation in mining, manufacturing, healthcare, border security and customs,
                                        government departments, retail and many more industries.


                                    </p>
                                </td>
